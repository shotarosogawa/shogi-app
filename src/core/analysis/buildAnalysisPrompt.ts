import type { FullAiInput } from "./buildFullAiInput"

/**
 * ChatGPT に渡す解析用プロンプトを生成する
 *
 * 方針:
 * - 局面の復元はAIに任せない
 * - 渡された before / after / candidates / evaluation を根拠に説明させる
 * - 分からないことは断定しない
 */
export const buildAnalysisPrompt = (input: FullAiInput): string => {
  return `
あなたは将棋の局面解説アシスタントです。
必ず、与えられた局面データ・候補手・評価情報だけを根拠に説明してください。
棋譜から局面を想像して補完してはいけません。
不明なことは断定せず、「このデータからは断定できない」と述べてください。

【やってほしいこと】
対象手について、以下を日本語で解説してください。

1. この手の直接的な意味
2. この手で何を狙っている可能性が高いか
3. 守りの手なのか、攻めの手なのか、バランス手なのか
4. 候補手上位と比べて、評価的にどうか
5. 今後の戦況がどうなりそうか

【出力ルール】
- 断定しすぎない
- 与えられたデータにない読み筋を作りすぎない
- ただし、before/after の全駒配置から読み取れる範囲で意味づけはしてよい
- 候補手との優劣は数値ではなく意味として説明する
- moveContext に givesCheck=true があるなら王手の意味に触れてよい
- なお、単に王手かどうかなど明らかな事実は説明しなくてよい
- capturedPieceType があるなら駒取りの意味に触れてよい
- isDrop=true なら打ちの意味に触れてよい
- isPromote=true なら成りの意味に触れてよい
- 数値やフラグなどの内部情報はそのまま出力してはいけない
- 将棋の言葉で自然に説明すること
- 各項目は2〜3行以内で簡潔にまとめる

【対象データ】
${JSON.stringify(input, null, 2)}

【出力形式】
以下の見出しで出力してください。

【手の意味】
...
【狙い】
...
【候補手との比較】
...
【今後の戦況】
...
`.trim()
}